    {   


        $data['invoice'] = Invoice::leftjoin('po','po.id','=','invoice.po_id')
                    ->leftjoin('users','users.id','=','invoice.sales_id')
                    ->select('invoice.*','po.number AS po_number','users.name AS sales_name')
                    ->get();

        return view('invoice/index',compact('data'));
    }

